<b>Secciones de P&aacute;ginas</b><br><br>

Las Secciones de P&aacute;ginas son grupos de P&aacute;ginas que se muestran en un grupo de men&uacute; por separado, 
y permite confeccionar el sitio web. Tienen un orden de presentaci&oacute;n en el sitio, y un indicador si el grupo est&aacute; oculto o no.<br><br>

El registro de una Secci&oacute;n de P&aacute;ginas consta de los campos siguientes:
<ul>
<li> <b>Secci&oacute;n</b>: literal descriptivo de la Secci&oacute;n y que aparece en el men&uacute;.
<li> <b>Imagen</b>: fichero de imagen a modo de icono que se muestra al mostrar la Secci&oacute;n.
<li> <b>Orden</b>: indica el &oacute;rden de presentaci&oacute;n de esta Secci&oacute;n en el men&uacute; con las restantes secciones.
<li> <b>Visible</b>: indica si la Secci&iacute;n se muestra en el men&uacute; de p&aacute;ginas o no, o es una Secci&oacute;n de Maquetas (solo visible por el Administrador y por los Usuarios con el Perfil predefinido de Editor).
<li> <b>Color</b>: color utilizado por la Gesti&oacute;n de Contenidos para mostrar la Secci&oacute;n.
</ul>

El detalle de una Secci&oacute;n nos muestra adem&aacute;s las P&aacute;ginas que contiene.<br>


